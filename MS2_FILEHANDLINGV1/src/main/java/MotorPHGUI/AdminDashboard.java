    private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton21ActionPerformed
        // Create a file chooser
    JFileChooser fileChooser = new JFileChooser();
    
    // Set the default directory to the user's home directory (e.g., Downloads, Desktop, etc.)
    fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
    
    // Filter for CSV files
    FileNameExtensionFilter filter = new FileNameExtensionFilter("CSV Files", "csv");
    fileChooser.setFileFilter(filter);
    
    // Show the file chooser dialog
    int returnValue = fileChooser.showOpenDialog(null);
    
    if (returnValue == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();
        String filePath = selectedFile.getAbsolutePath();
        
        try {
            // Read the CSV file
            BufferedReader br = new BufferedReader(new FileReader(filePath));
            String line;
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            // Clear the table before loading new data
            model.setRowCount(0);
            
            // Read the header (first line)
            String[] headers = br.readLine().split(",");
            model.setColumnIdentifiers(headers);
            
            // Read the data rows
            while ((line = br.readLine()) != null) {
                String[] rowData = line.split(",");
                model.addRow(rowData);
            }
            
            br.close();
            JOptionPane.showMessageDialog(null, "CSV file loaded successfully!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error loading CSV file: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    }//GEN-LAST:event_jButton21ActionPerformed

    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
      // Get the keyword from jTextField1
    String keyword = jTextField1.getText().trim().toLowerCase();

    // Get the table model and row sorter
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    jTable1.setRowSorter(sorter);

    // If the keyword is empty, reset the filter
    if (keyword.isEmpty()) {
        sorter.setRowFilter(null);
    } else {
        // Create a row filter to match the keyword in any column
        RowFilter<DefaultTableModel, Object> rowFilter = new RowFilter<DefaultTableModel, Object>() {
            @Override
            public boolean include(Entry<? extends DefaultTableModel, ? extends Object> entry) {
                for (int i = 0; i < entry.getValueCount(); i++) {
                    String cellValue = entry.getStringValue(i).toLowerCase();
                    if (cellValue.contains(keyword)) {
                        return true;
                    }
                }
                return false;
            }
        };
        sorter.setRowFilter(rowFilter);
    }
    }//GEN-LAST:event_jButton15ActionPerformed

